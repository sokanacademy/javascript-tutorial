function multiplication(num1, num2) {
  /**
   * This function calculates num1*num2
   * @param {number} num1 first number
   * @param {number} num2 second number
   * @return {number}     num1*num2
   */
  return num1 * num2;
}

var num1 = 2;
var num2 = 3;
console.log("num1 * num2 =", multiplication(num1, num2));
